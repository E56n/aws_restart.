# Ejemplo de Lista
myFruitList = ["apple", "banana", "cherry"]
print("Lista:", myFruitList)
myFruitList[2] = "orange"  # Modificar valor en la lista
print("Lista modificada:", myFruitList)

# Ejemplo de Tupla
myFruitTuple = ("apple", "banana", "pineapple")
print("\nTupla:", myFruitTuple)

