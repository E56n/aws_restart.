# Demostración de tipos de datos numéricos en Python

# Mensaje inicial sobre los tipos de datos numéricos
print("Python tiene tres tipos de datos numéricos: entero (int), flotante (float) y complejo (complex)")

# Demostración del tipo de dato entero (int)
miValor = 1
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de dato " + str(type(miValor)))

# Demostración del tipo de dato flotante (float)
miValor = 3.14
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de dato " + str(type(miValor)))

# Demostración del tipo de dato complejo (complex)
miValor = 5j
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de dato " + str(type(miValor)))

# Demostración del tipo de dato booleano (bool) - Verdadero
miValor = True
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de dato " + str(type(miValor)))

# Demostración del tipo de dato booleano (bool) - Falso
miValor = False
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de dato " + str(type(miValor)))